import pytest

# Реалізація алгоритму для контексту виконання тестів
def bubble_sort(arr):
    n = len(arr)
    for i in range(n):
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
    return arr

def test_base_case():
    # Тест для n = n0 (базовий випадок доведення)
    # Ілюструє етап 'Initialization': перевіряє, що для мінімально допустимого параметра n = 0 
    # (порожній масив) інваріант виконується тривіально, а алгоритм повертає коректну структуру.
    
    # Виклик функції з мінімальним допустимим n
    result = bubble_sort([])
    
    # Assert очікуваного результату
    assert result == []

def test_inductive_step_example():
    # Тест, що ілюструє перехід P(k) → P(k+1)
    # Ілюструє етап 'Maintenance': показує, що якщо властивість P(n) є істинною для масиву 
    # розмірністю k, то розширення структури до розміру k+1 також підтримує закон збереження 
    # інваріанта, і алгоритм успішно упорядковує елементи за зростанням.
    
    k = 3
    arr_k = [3, 1, 2]
    arr_k_plus_1 = [3, 1, 2, 0]
    
    # Виклик функції для k і k+1
    res_k = bubble_sort(arr_k)
    res_k_plus_1 = bubble_sort(arr_k_plus_1)
    
    # Порівняння результатів та assert математичної властивості відсортованості
    assert res_k == [1, 2, 3]
    assert res_k_plus_1 == [0, 1, 2, 3]
    
    # Пряма перевірка математичного твердження P(n): res[k] <= res[k+1]
    assert all(res_k[i] <= res_k[i+1] for i in range(len(res_k) - 1))
    assert all(res_k_plus_1[i] <= res_k_plus_1[i+1] for i in range(len(res_k_plus_1) - 1))

def test_boundary_value():
    # Тест для граничного або нетривіального значення
    # Ілюструє етап 'Termination': перевіряє поведінку алгоритму в найгіршому сценарії (worst-case), 
    # коли вхідний масив повністю зворотний. Це доводить, що при виході з циклу (і = n) комбінація 
    # інваріанта та умови завершення гарантує точне виконання фінальної специфікації програми.
    
    # Виклик функції для граничного значення
    boundary_arr = [5, 4, 3, 2, 1]
    result = bubble_sort(boundary_arr)
    
    # Assert очікуваного результату
    assert result == [1, 2, 3, 4, 5]